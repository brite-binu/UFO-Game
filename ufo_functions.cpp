void ufo_functions::end_game(string answer, string codeword)
{
	if (answer == codeword)
	{
		cout << "Hooray! You saved the person and earned a medal of honor!" << endl;
	}
	else
	{
		cout << "Oh no! The UFO just flew away with another person!" << endl;
	}
}

void ufo_functions::display_status(vector <char> incorrect, string answer)
{
	cout << "Incorrect Guesses:" << endl;
	for (int i = 0; i < incorrect.size(); i++)
	{
		cout << incorrect[i] << " ";
	}
	cout << endl << "Codeword:";
	for (int j = 0; j < answer.length(); j++)
	{
		cout << answer[j] << " ";
	}
	cout << endl;
}
